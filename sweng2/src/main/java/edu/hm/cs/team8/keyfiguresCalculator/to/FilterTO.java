package edu.hm.cs.team8.keyfiguresCalculator.to;

import javax.validation.constraints.NotNull;

public class FilterTO {

	@NotNull
	private String name;

	@NotNull
	private String value;
	
	public FilterTO() {
	}
	
	public FilterTO(String name, String value)
	{
		this.name = name;
		this.value = value;
		
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FilterTO other = (FilterTO) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public String toString() {
		return "FilterTO [name=" + name + ", value=" + value + "]";
	}

}
